#include "3-calc.h"

/**
 * op_add - adds to integers
 * @a: first number
 * @b: second number
 *
 * Return: result int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts to integers
 * @a: first number
 * @b: second number
 *
 * Return: result int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies to integers
 * @a: first number
 * @b: second number
 *
 * Return: result int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides to integers
 * @a: first number
 * @b: second number
 *
 * Return: result int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - performs modulo on two integers and calculates reminder
 * @a: first number
 * @b: second number
 *
 * Return: result int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
